import { execFileSync } from 'node:child_process'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import path from 'node:path'
import { WORKFLOW_SOURCE_EDIT_CODE_PART_ADAPTER } from '../shared/workflowSourceEditCodePartAdapter.js'
import {
  compareSourceTwinText,
  parseGeneratedFunctionalContextPack,
  parseGeneratedSourceTwin,
  readSourceRepositoryMetadata,
  readSourceTwinWorkingTree,
  runSourceLensWorkflow,
  serializeSourceCodePartManifest,
  serializeSourceFlowManifest,
  serializeFunctionalContextPack,
  serializeSourceTwinManifest,
  SOURCE_CODE_PART_MANIFEST_PATH,
  SOURCE_FEATURE_MANIFEST_PATH,
  SOURCE_FLOW_MANIFEST_PATH,
  SOURCE_FUNCTIONAL_CONTEXT_MANIFEST_PATH,
  SOURCE_TWIN_MANIFEST_PATH,
} from './source-lens-engine.mjs'

function firstDifference(existing, expected, pointer = '$') {
  if (Object.is(existing, expected)) return null
  if (Array.isArray(existing) || Array.isArray(expected)) {
    if (!Array.isArray(existing) || !Array.isArray(expected)) return { pointer, existing, expected }
    if (existing.length !== expected.length) {
      return { pointer: `${pointer}.length`, existing: existing.length, expected: expected.length }
    }
    for (let index = 0; index < expected.length; index += 1) {
      const difference = firstDifference(existing[index], expected[index], `${pointer}[${index}]`)
      if (difference) return difference
    }
    return null
  }
  if (existing && expected && typeof existing === 'object' && typeof expected === 'object') {
    const keys = [...new Set([...Object.keys(existing), ...Object.keys(expected)])].sort(compareSourceTwinText)
    for (const key of keys) {
      const difference = firstDifference(existing[key], expected[key], `${pointer}.${key}`)
      if (difference) return difference
    }
    return null
  }
  return { pointer, existing, expected }
}

function diagnosticValue(value) {
  const serialized = JSON.stringify(value)
  if (serialized === undefined) return String(value)
  return serialized.length > 240 ? `${serialized.slice(0, 237)}...` : serialized
}

const root = path.resolve(fileURLToPath(new URL('../', import.meta.url)))
const target = path.join(root, SOURCE_TWIN_MANIFEST_PATH)
const featureTarget = path.join(root, SOURCE_FEATURE_MANIFEST_PATH)
const codePartTarget = path.join(root, SOURCE_CODE_PART_MANIFEST_PATH)
const flowTarget = path.join(root, SOURCE_FLOW_MANIFEST_PATH)
const functionalContextTarget = path.join(root, SOURCE_FUNCTIONAL_CONTEXT_MANIFEST_PATH)
const checkOnly = process.argv.includes('--check')
const prepareOnly = process.argv.includes('--prepare')
let previous = null
let previousFunctionalContextPack = null
try {
  previous = parseGeneratedSourceTwin(execFileSync(
    'git',
    ['show', `HEAD:${SOURCE_TWIN_MANIFEST_PATH}`],
    { cwd: root, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'], maxBuffer: 20 * 1024 * 1024 },
  ))
} catch {}
try {
  previousFunctionalContextPack = parseGeneratedFunctionalContextPack(execFileSync(
    'git',
    ['show', `HEAD:${SOURCE_FUNCTIONAL_CONTEXT_MANIFEST_PATH}`],
    { cwd: root, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'], maxBuffer: 4 * 1024 * 1024 },
  ))
} catch {}

const {
  manifest,
  functionalContextPack,
  featureModel,
  codePartCatalog,
  flowCatalog,
} = runSourceLensWorkflow({
  files: readSourceTwinWorkingTree(root),
  previous,
  previousFunctionalContextPack,
  repository: readSourceRepositoryMetadata(root),
  outputs: { featureModel: true, codeParts: true, flows: true },
  artifactAdapters: { codePartAnnotation: WORKFLOW_SOURCE_EDIT_CODE_PART_ADAPTER },
})
const generated = serializeSourceTwinManifest(manifest)
const existing = existsSync(target) ? readFileSync(target, 'utf8') : ''
const existingManifest = parseGeneratedSourceTwin(existing)
const preparedManifest = existingManifest
  ? { ...manifest, changeSet: existingManifest.changeSet }
  : manifest
const prepared = serializeSourceTwinManifest(preparedManifest)
const featureGenerated = `// Generated by npm run source-twin:update. Do not edit by hand.\nexport const SOURCE_FEATURE_MANIFEST = ${JSON.stringify(featureModel, null, 2)}\n`
const featureExisting = existsSync(featureTarget) ? readFileSync(featureTarget, 'utf8') : ''
const codePartGenerated = serializeSourceCodePartManifest(codePartCatalog)
const codePartExisting = existsSync(codePartTarget) ? readFileSync(codePartTarget, 'utf8') : ''
const flowGenerated = serializeSourceFlowManifest(flowCatalog)
const flowExisting = existsSync(flowTarget) ? readFileSync(flowTarget, 'utf8') : ''
const functionalContextExisting = existsSync(functionalContextTarget)
  ? readFileSync(functionalContextTarget, 'utf8')
  : ''
const existingFunctionalContextPack = parseGeneratedFunctionalContextPack(functionalContextExisting)
const preparedFunctionalContextPack = existingFunctionalContextPack?.fingerprint === functionalContextPack.fingerprint
  ? existingFunctionalContextPack
  : functionalContextPack
const functionalContextGenerated = serializeFunctionalContextPack(functionalContextPack)
const functionalContextPrepared = serializeFunctionalContextPack(preparedFunctionalContextPack)

if (checkOnly) {
  // changeSet intentionally describes the parent-to-current commit delta. On a
  // clean deployment HEAD already contains the generated manifest, so
  // recalculating against HEAD would erase that historical delta. Validate all
  // deterministic source fields while preserving the committed delta envelope.
  if (prepared !== existing) {
    console.error('Source twin manifest is stale. Run: npm run source-twin:update')
    const difference = firstDifference(existingManifest, preparedManifest)
    console.error(`Committed manifest: ${existingManifest?.id ?? 'unreadable'}`)
    console.error(`Generated manifest: ${manifest.id}`)
    if (difference) {
      console.error(`First difference: ${difference.pointer}`)
      console.error(`Committed value: ${diagnosticValue(difference.existing)}`)
      console.error(`Generated value: ${diagnosticValue(difference.expected)}`)
    }
    process.exitCode = 1
  }
  if (featureGenerated !== featureExisting) {
    console.error('Source feature manifest is stale. Run: npm run source-twin:update')
    process.exitCode = 1
  }
  if (codePartGenerated !== codePartExisting) {
    console.error('Source code-part manifest is stale. Run: npm run source-twin:update')
    process.exitCode = 1
  }
  if (flowGenerated !== flowExisting) {
    console.error('Source flow manifest is stale. Run: npm run source-twin:update')
    process.exitCode = 1
  }
  if (functionalContextPrepared !== functionalContextExisting) {
    console.error('Source functional-context manifest is stale. Run: npm run source-twin:update')
    process.exitCode = 1
  }
  if (
    prepared === existing
    && featureGenerated === featureExisting
    && codePartGenerated === codePartExisting
    && flowGenerated === flowExisting
    && functionalContextPrepared === functionalContextExisting
  ) {
    console.log(`Source twin manifest is current: ${manifest.id}`)
  }
} else if (prepareOnly) {
  if (prepared !== existing) writeFileSync(target, prepared)
  if (featureGenerated !== featureExisting) writeFileSync(featureTarget, featureGenerated)
  if (codePartGenerated !== codePartExisting) writeFileSync(codePartTarget, codePartGenerated)
  if (flowGenerated !== flowExisting) writeFileSync(flowTarget, flowGenerated)
  if (functionalContextPrepared !== functionalContextExisting) {
    writeFileSync(functionalContextTarget, functionalContextPrepared)
  }
  console.log(`Prepared ${SOURCE_TWIN_MANIFEST_PATH}: ${manifest.id}${prepared !== existing ? ' (refreshed)' : ''}`)
} else {
  writeFileSync(target, generated)
  writeFileSync(featureTarget, featureGenerated)
  writeFileSync(codePartTarget, codePartGenerated)
  writeFileSync(flowTarget, flowGenerated)
  writeFileSync(functionalContextTarget, functionalContextGenerated)
  console.log(`Updated ${SOURCE_TWIN_MANIFEST_PATH}: ${manifest.id}`)
}
